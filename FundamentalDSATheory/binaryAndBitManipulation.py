# Bitwise AND (&)
print(5 & 7)

# Bitwise OR (!)
print(5 | 7)

# Bitwise XOR (^)
print(5 ^ 7)

# Bitwise NOT (~)
print(~5)

# Bitwise left shift (<<)
print(5 << 2)

# Bitwise right shift (<<), (aritmetic and signed)
print(5 >> 1)

# Hexadecimals (base 16)
print(hex(5)) #0x5
print(hex(11))
print(hex(25)) # 16 + 9
print(hex(890))